#import "Main.h"

//==================================================================================================
@implementation Main
//--------------------------------------------------------------------------------------------------
+ (void)inject
{   
    [Main sharedInstance];
    
    NSLog(@"Injecting Safari from: %@", [[NSBundle bundleForClass: [Main class]] bundlePath]);
}
//--------------------------------------------------------------------------------------------------
+ (Main *)sharedInstance
{
    static Main *injection = nil;
    
    if (nil == injection)
        injection = [[Main alloc] init];
    
    return injection;
}
//--------------------------------------------------------------------------------------------------
